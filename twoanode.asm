ORG 00h
	
	RELOAD:
	MOV A, #0FFh
	MOV P1, A
	MOV DPTR, #CODEDATA
	MOV R0, #05h
	MOV B, #01h	
	MOV R1, #00h
	
	REPEAT:
	MOV A, R1
	MOVC A, @A+DPTR
	MOV P1, A
	INC R1
	MOV P3, B
	MOV A, B
	RL A
	MOV B, A
	ACALL DELAY
	DJNZ R0, REPEAT
	SJMP RELOAD
	
	DELAY: 
	MOV R5, #50
	DEL1: MOV R6, #50
	DEL2: MOV R7, #50
	DEL3: DJNZ R7, DEL3
		  DJNZ R6, DEL2
	      DJNZ R5, DEL1
		  RET

	CODEDATA: DB 0F9h, 0E3h, 83h, 0A0h, 87h	
END