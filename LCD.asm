ORG 00h
	MOV DPTR, #CNTWRD
	
	NEWCMD:
	MOV A, #00h
	MOVC A, @A+DPTR
	JZ DATAOUT
	MOV P1, A
	CLR P3.0
	CLR P3.1
	SETB P3.2
	ACALL DELAY
	CLR P3.2
	INC DPTR
	SJMP NEWCMD
	
	DATAOUT:
	MOV DPTR, #MYDAAT
	MOV A, #00h
	MOVC A, @A+DPTR
	JZ STOP
	SETB P3.0
	CLR P3.1
	SETB P3.2
	ACALL DELAY
	CLR P3.2
	INC DPTR
	SJMP DATAOUT
	STOP: SJMP STOP
	
	DELAY: MOV R3,#250 	;50 or higher 
	HERE2: MOV R4,#255 	;R4 = 255
	HERE: DJNZ R4,HERE 	;stay until R4 becomes 0
	DJNZ R3,HERE2
	RET
	
	CNTWRD:
	DB 38h, 0Eh, 01h, 06h, 80h, 0
	
	MYDAAT:
	DB 'IUBAT', 0
	
	END