ORG 00h
	START:
	MOV R0, #11
	MOV DPTR, #TABLE
	
	LOOP:
	MOV A, #00h
	MOVC A, @A+DPTR
	MOV P1, A
	INC DPTR
	ACALL DELAY
	DJNZ R0, LOOP
	SJMP START
	
	DELAY:
	MOV R1, #95h
	DEL1:
	MOV R2, #60h
	DEL2:
	MOV R3, #20h
	DEL3:
	DJNZ R3, DEL3
	DJNZ R2, DEL2
	DJNZ R1, DEL1
	RET
	
	TABLE:
	DB 0C0h, 0F9h, 0A4h, 0B0h, 99h, 92h, 82h, 0F8h, 80h, 90h, 7Fh
	
	END